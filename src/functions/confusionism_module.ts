export function randomIntegerBetweenXandY(min: number, max: number): number {
    return Math.floor(Math.random() * (max - min) ) + min;
}

const generateRandomString = (lengthOfGeneratedRandomString: number, lowerCased: boolean = true): string => {
    const upperCaseAlphabeth = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
    const lowerCaseAlphabeth = upperCaseAlphabeth.toLowerCase();

    let randomString = "";
    for (let i = 0; i < lengthOfGeneratedRandomString - 1; i++) {
        lowerCased ?
            randomString += lowerCaseAlphabeth[randomIntegerBetweenXandY(0, 26)]
            :
            randomString += upperCaseAlphabeth[randomIntegerBetweenXandY(0, 26)];
    }
    return randomString;
}

const simplifiedPhraseIntoHiddenCharactersByLine = ( simplifiedString: string, numberOfCharsByLine: number = 10, spacesBetween: boolean = true): string => {
   
    const words = simplifiedString.split(" ");
    let finalString = '';
    words.forEach( (word) => {
        let blockOfWordHiddenChars = '';
        word.split('').forEach( (char) => {
        const randomPositionOfChar = randomIntegerBetweenXandY(0, numberOfCharsByLine - 1);
        let placeHolderWord = generateRandomString(10);
        let tempWordArray = placeHolderWord.split('');
        tempWordArray[randomPositionOfChar] = char;
        let hiddenCharWord = tempWordArray.join('');
        blockOfWordHiddenChars += hiddenCharWord + '\n';
        })

        finalString += blockOfWordHiddenChars + (spacesBetween ? '\n' : '');


    });
    return finalString;
}

export function wordToWordPuzzleRow( asciiLettersWord: string, numberOfLettersInTheRow: number): string {
    const alphabeth = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
    const lastIndexWereWordCanStart = numberOfLettersInTheRow - asciiLettersWord.length;
    const numberOfPostionsToFillInRow = lastIndexWereWordCanStart;
    const numberOfLettersBeforeWord = randomIntegerBetweenXandY(0, numberOfPostionsToFillInRow);
    const numberOfLettersAfterWord = numberOfPostionsToFillInRow - numberOfLettersBeforeWord;

    let wordWithLettersBeforeAndAfter = "";
    for (let i = 1; i <= numberOfLettersBeforeWord; i++ ) {
        const indexOfRandomLetter = randomIntegerBetweenXandY(0, 25);
        wordWithLettersBeforeAndAfter += alphabeth[indexOfRandomLetter];
    }

    wordWithLettersBeforeAndAfter += asciiLettersWord;

    for (let i = 1; i <= numberOfLettersAfterWord; i++ ) {
        const indexOfRandomLetter = randomIntegerBetweenXandY(0, 25);
        wordWithLettersBeforeAndAfter += alphabeth[indexOfRandomLetter];
    }


    return wordWithLettersBeforeAndAfter;
}

export function textToWordPuzzle(originalText: string, numberOfPaddingLetters: number): string {

    const simplefiedText = asciiLettersAndSpaceCharsOnly(originalText);
    const words = simplefiedText.split(' ');
    const lengthOfBiggestWord = Math.max(...words.map((word) => word.length));

    let wordPuzzleFullText = '';
    const lengthOfRow = lengthOfBiggestWord + numberOfPaddingLetters;

    words.forEach((word) => {
        wordPuzzleFullText += wordToWordPuzzleRow(word, lengthOfRow) + '\n'
    });


    return simplefiedText ? wordPuzzleFullText.toUpperCase() : "";
}

