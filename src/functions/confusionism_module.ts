/**
 * Replace the vowel in a string with a character,
 * for example '*', or another string
 *
 * @param word - The original string
 * @param charToReplaceVowels - The character or string to replace the vowels
 * @returns The string with vowels replaced by the character or string
 *
 */
export function replaceVowels( word: string, charToReplaceVowels: string ): string {

    return word.replace(/a|á|à|â|ã|e|é|è|ê|i|í|o|ó|ô|u|ú/gi, charToReplaceVowels);
}

// PONTUACTION CHARACTERS AT THE END
const pontuationCharacters:string = '.!?,';
export function pontuationCharacterAtTheEnd( text: string ):boolean {
    return pontuationCharacters.includes(
        text[text.length-1]
    );
}

// PONTUACTION CHARACTERS AT THE START
export function pontuationCharacterAtTheStart( text: string ): boolean {
    return pontuationCharacters.includes(text[0]);
}

// REVERSE WORD
export function reverseWord( wordToReverse: string ): string {
    let lastCharacter= '';
    if (pontuationCharacterAtTheEnd( wordToReverse )) {
        lastCharacter = wordToReverse[wordToReverse.length-1];
        wordToReverse = wordToReverse.substring(0, wordToReverse.length-1);
    }
    return wordToReverse.split('').reverse().join('') + lastCharacter;
}

export function reverseAllWords(text: string): string {
    let textWithAllReversedWords = "";
    let words = text.split(/\s/gi);
    words.forEach((word) => {
        textWithAllReversedWords += reverseWord(word) + " ";
    })
    return textWithAllReversedWords.trimEnd();
}

// SHUFFLE WORD
export function shuffleWord( word: string ): string {

    if (word.length === 1) return word;
    if (word.length === 2) return reverseWord(word);

    let lastCharacter = '';

    if (pontuationCharacterAtTheEnd( word )) {
        lastCharacter = word[word.length-1];
        word = word.substring(0, word.length-1);
    }

    const originalWord  = word;
    let shuffledWord    = word.split('').sort(() => Math.random() - 0.5).join('');
    if (shuffledWord === word) {
        shuffledWord    = word.split('').sort(() => Math.random() - 0.5).join('');
    }
    if (shuffledWord === word) {
        shuffledWord    = word.split('').sort(() => Math.random() - 0.5).join('');
    }
    if (shuffledWord === word) {
        shuffledWord    = word.split('').sort(() => Math.random() - 0.5).join('');
    }

    shuffledWord += lastCharacter;

    return shuffledWord;
}

export function shuffledWordsText(originalText: string): string {
    const words = originalText.split(/\s/g);
    let shuffledWordsText = "";
    words.forEach( (word) => {
        let shuffledWord = shuffleWord(word);
        shuffledWordsText += shuffledWord + " ";
    })
    return shuffledWordsText.trimEnd();
}


// LETTERS TO NUMBERS
export function lettersToNumbers( upperCasedWord: string ): string {

    let changedWord = upperCasedWord;
    changedWord = 
        changedWord
        .replace(/A|Á|À|Â|Ã/gi, '4')
        .replace(/E|É|È/gi, '3')
        .replace(/I|Í|Ì/gi, '1')
        .replace(/O|Ó|Ò/gi, '0')
        .replace(/S/gi, '5')
        .replace(/G/gi, '6')
        .replace(/T/gi, '7');
    return changedWord;
}

// TEXT TO ASCII LETTERS AND SPACE ONLY
export function asciiLettersAndSpaceCharsOnly(stringToEncript: string): string {
    let onlyAsciiLettersMessage = '';

    onlyAsciiLettersMessage =
        stringToEncript
        .replace(/á|à|â|ã/gi, 'a')
        .replace(/é|è|ê/gi, 'e')
        .replace(/í|ì/gi, 'i')
        .replace(/ó|ò|ô/gi, 'o')
        .replace(/ú/gi, 'u')
        .replace(/ç/gi, 'c')
        .replace(/-/gi, ' ')
        .replace(/[^A-Za-z]/gi, ' ')
        .replace(/\s\s/gi, ' ')
        .trim();

    return onlyAsciiLettersMessage;
}

