// VOWELS TO AIX ENDER INIX OVER UFUX
export function aixToUfux( word:string ): string {

    const key = 'A --> AIX \ta --> aix\nE --> ENDER \te --> ender\nI --> INIX \ti --> inix\nO --> OVER \to --> over\nU --> UFUX \tu --> ufux\n\n';

    let changedWord = '';

    for ( let i = 0; i < word.length; i++) {
        switch(word[i]) {

            case 'a':
            case 'á':
            case 'à':
            case 'â':
            case 'ã':
            changedWord += 'aix';
                break;
            case 'A':
            case 'Á':
            case 'À':
            case 'Â':
            case 'Ã':
            changedWord += 'AIX';
                break;
            
            case 'e':
            case 'é':
            case 'è':
            changedWord += 'ender';
                break;
            case 'E':
            case 'É':
            case 'È':
            changedWord += 'ENDER';
                break;
            
            case 'i':
            case 'í':
            case 'ì':
            changedWord += 'inix';
                break;
            case 'I':
            case 'Í':
            case 'Ì':
            changedWord += 'INIX';
                break;
            
            case 'o':
            case 'ó':
            case 'ò':
            changedWord += 'over';
                break;
            case 'O':
            case 'Ó':
            case 'Ò':
            changedWord += 'OVER';
                break;
            
            case 'u':
            changedWord += 'ufux';
                break;
            case 'U':
            changedWord += 'UFUX';
                break;
            
            default:
            changedWord += word[i];
                break;
        }
    }

    return changedWord ? key + changedWord : "";
}

